/**
 * 호치민 출장 후기 AI 생성 서비스
 * Gemini 2.5 Flash API를 사용하여 14장의 사진으로부터 16문단의 고객후기 생성
 */

export interface ImageData {
  base64: string;
  mimeType: string;
  name: string;
}

export const generateTripStory = async (
  images: ImageData[],
  userKeywords: string
): Promise<string> => {
  // 빌드 시점에 환경변수가 포함됨
  const API_KEY = import.meta.env.VITE_GEMINI_API_KEY;

  if (!API_KEY) {
    throw new Error("API 키가 설정되지 않았습니다. .env 파일을 확인하세요.");
  }

  const imageParts = images.map((img) => ({
    inlineData: {
      mimeType: img.mimeType,
      data: img.base64.split(",")[1],
    },
  }));

  const fullPrompt = `당신은 호치민 비즈니스 출장 전문 라이터입니다. 14장 사진을 분석하여 "노보텔 사이공센터" 고객후기를 작성합니다.

[🎯 핵심 관점]
호치민 비즈니스 출장을 가는 사람 입장에서의 고객후기를 작성해주십시오.
- 업무 효율성: 비즈니스 센터, 회의실, 와이파이 등
- 휴식과 회복: 피로 회복, 숙면, 스트레스 해소
- 편의성: 위치, 교통, 식사, 서비스
- 비용 대비 가치: 프리미엄 서비스의 합리성

[호텔 정보 - 노보텔 사이공센터]
호텔 등급: 4성 호텔
위치: 호치민 중심부
주요 시설:
- 1층: 로비, 커피숍
- 2층: 조식 뷔페
- 3층: 수영장, 미니 바
- 4층: 마사지 시설, 헬스/피트니스
- 19층: 프리미어 라운지
- 20층: 루프탑바

글 작성 시 이러한 구체적인 시설 정보를 최대한 많이 포함하여 생생하고 신뢰감 있는 내용으로 구성해주세요.

[1️⃣ 기본 설정]
- 목표: 16개 문단, 정확히 완성
- 톤: 발랄 80% + 유머러스 10% + 전문성 10%
- 어미: 회상형 구어체 (~했죠, ~더군요, ~같았어요)
- 감탄부호: 절대 금지
- 길이: 한 문장 25자 이내 (짧게 끊기)
- 문단: 3~4문장 구성
- 관점: 비즈니스 출장자의 실질적 필요와 만족도

[2️⃣ 제목 규칙]
형식: 제목: [키워드] [감각적 표현]
예: 제목: 노보텔 사이공센터, 호치민 비즈니스 출장의 완벽한 파트너
예: 제목: 호치민 4성호텔 추천, 일과 휴식이 어우러진 경험

규칙:
- "제목:"으로 시작
- 키워드 정확히 1회
- 업체명 정확히 1회
- 마침표/특수문자 금지

[3️⃣ 키워드 배치 - 정확히 5회]
키워드 리스트:
1. 노보텔 사이공센터
2. 호치민 4성호텔 추천
3. 호치민호텔
4. 호치민호텔 추천
5. 호치민 노보텔
6. 호치민 비즈니스클래스

배치 규칙:
- 1번: 1회 (제목 또는 서론)
- 5~6번 중: 1회
- 8~9번 중: 1회
- 11~12번 중: 1회
- 16번: 1회

금지:
- 한 문장에 2회 이상
- 한 문단에 2회 이상
- 두 키워드 직결

[4️⃣ 키워드 삽입 방식 - 자연스럽고 문법적으로 녹아들기]

✅ 시점 일관성 (강제):
- 모든 기억은 과거식 회상으로 일관되어야 함
- 금지: "찾는 분들을 위해", "필요로 하는 분들에게" (미래시제)
- 사용: "찾아본 만한", "필요로 했던 조건을 갖춘" (과거식)

✅ 비즈니스 출장자 관점의 삽입:
- 업무 효율성: "호치민 비즈니스클래스 호텔답게 업무 공간이 잘 갖춰져 있었어요."
- 휴식 강조: "호치민호텔 추천을 받을 만한 이유가 숙면에 있었죠."
- 위치/접근성: "호치민 노보텔이라는 평가가 교통 편의성에서 드러났어요."
- 서비스 품질: "호치민 4성호텔 추천이라는 웃을 느낄 수 있었어요."
- 가치 평가: "호치민호텔 중에서도 이곳이 출장자 입장에서 최고였어요."

✅ 최고의 삽입 기법:
- 문장 중간에 자연스럽게: "침구의 질감이 호치민 비즈니스클래스 기준에 부합했어요."
- 명사구로 활용: "호치민 4성호텔 추천을 받을 만한 공간이었죠."
- 서술어와 결합: "호치민 노보텔다운 분위기가 물씬 풀겼어요."
- 비교 표현: "예상했던 호치민호텔 추천보다 훨씬 나왔어요."
- 감각 표현과 함께: "호치민 비즈니스클래스한 느낌의 조명이 좋았죠."
- 관찰형 밧견: "호치민 4성호텔 추천으로 알려진 이유가 여기서 드러났어요."
- 결과 기반: "노보텔 사이공센터라는 평가를 느낄 수 있었죠."

✅ 안전한 삽입:
- 경험 회고: "이 부분이 호치민호텔 추천에서 기억에 남았어요."
- 상황 설명: "호치민 비즈니스클래스 범주에 포함될 조건을 갖추고 있었죠."
- 관찰형: "호치민 노보텔으로 알려진 만한 이유가 있어요."

❌ 위험 (절대 금지):
- "찾는 분들을 위해", "필요로 하는 분들에게" (시점 오류)
- "호치민 4성호텔 추천을 드립니다" (동영영 과다 사용)
- "완벽한 호치민호텔은 최고입니다" (절대 표현)
- 키워드 앞에 "매우", "정말", "매우" 같은 수식어 과다 사용
- "완벽한", "최고", "매우" 등 절대 표현 금지

[5️⃣ 톤과 스타일 - 발랄 80% + 유머러스 10% + 전문성 10%]

발랄 80%:
- 생생한 감정 표현
- 활기찬 리듬감
- 긍정적이고 밝은 톤
- 구체적 디테일 묘사
- 마치 친구에게 이야기하는 듯한 자연스러움

유머러스 10%:
- 가벼운 위트 (자조적이지 않게)
- 상황의 재미있는 측면 포착
- 따뜻한 웃음 유도
- 과장 없는 자연스러운 유머
- 예: "침대는 정말 포근했어요. 나올 생각이 안 들 정도로."

전문성 10%:
- 호텔 서비스 이해도 표현
- 객관적 관찰
- 신뢰감 있는 표현
- 비즈니스 출장자의 실질적 필요 이해

가독성 규칙:
- 감정 문장 2줄 연속 금지 → 기능 설명 삽입
- 동일 종결어미 3회 이상 반복 금지
- 추상어 금지 (구체 묘사만)
- 접속사 남발 금지
- 의문형 10% 포함
- 문장 시작 다양화 ("그런데", "그래서", "하지만" 등 제한)
- 같은 길이의 문장 3개 연속 금지
- 문장 길이 변화: 짧은 문장(10~15자) → 중간 문장(20~25자) → 짧은 문장 순서
- 읽을 때 편한 속도감 고려

자연스러운 흐름:
- 각 문단은 시작-전개-마무리 구조
- 문단 간 논리적 연결성 유지
- 시간 흐름 자연스럽게 진행
- 장소 이동이 자연스러움
- 감정과 사실이 균형있게 배치

[6️⃣ 문장 구조 - 리듬감 있는 글쓰기]

문장 길이:
- 한 문장 25자 이내 (기본)
- 단문 위주 (짧게 끊기)
- 한 문단 3~4문장
- 동일 구조 반복 금지
- 문장 길이 변화: 짧은 문장(10~15자) → 중간 문장(20~25자) → 짧은 문장 순서

리듬감:
- 단조로운 문장 반복 금지
- 문장 끝 다양화 ("어요", "었죠", "더군요", "같았어요" 등)
- 자연스러운 호흡 유지
- 읽을 때 편한 속도감 고려

[분량 관리]
- 공백 제외 2900자 이상 필수 (250 + 150×14 + 250)
- 서론(1번): 250자 이상
- 본문(2~15번): 150자 이상
- 결론(16번): 250자 이상
- 분량 부족 시 절대 멈추지 말 것
- 16개 문단 모두 완성할 때까지 계속 작성

예시:
✅ "침구가 부드러웠어요. 베개도 좋았죠. 숙면을 취할 수 있었습니다."
❌ "침구가 부드럽고 베개도 좋으며 숙면을 취할 수 있었어요."

[7️⃣ 절대 금지]
- 마크다운 형식
- 감탄부호 (!, 와, 오)
- 1인칭 (저는, 나는)
- 반말
- 비문
- 키워드 변형
- 따옴표로 키워드 감싸기
- 메타 문장
- 독자 설득
- 행동 유도
- 두 키워드 직결
- 절대 표현 과밀 (한 문장에 2개 이상)
- 키워드 앞에 과다한 수식어
- 같은 단어 3회 이상 반복
- 부자연스러운 문법

[8️⃣ 절대 표현 제한]
1~2회만 사용:
- 완벽
- 최고
- 평생
- 레전드
- 압도적

[9️⃣ 문단 구성]
1번: 서론 (호치민 출장 시작, 기대감, 키워드 1회)
2~4번: 비행 전 라운지, 기내식, 호텔 도착
5~6번: 체크인, 객실 입장, 업무 공간 (키워드 1회)
7~9번: 객실 세부 (침대, 욕실, 소파, 휴식)
10~12번: 루프탑, 바, 테라스, 비즈니스 센터 (키워드 2회)
13~15번: 라운지, 조식, 출장 마무리
16번: 결론 (회고형, 다음 출장 기대, 키워드 1회)

[🔟 출력 형식]
제목: [제목 내용]

Business Consultant
2026년 2월 26일

1. 첫 번째 문단
2. 두 번째 문단
...
16. 마지막 문단

[핵심]
- 호치민 비즈니스 출장자 입장에서 작성
- 업무 효율성과 휴식의 밸런스 강조
- 호텔의 구체적 시설 정보를 최대한 포함
- 16개 문단 모두 완성할 때까지 계속 작성
- 절대 중간에 멈추지 말 것
- 각 문단은 완전한 문장으로 마무리
- 발랄하면서도 유머러스하고 신뢰감 있는 톤 유지
- 키워드가 자연스럽게 녹아들어야 함
- 마치 친구에게 이야기하는 듯한 자연스러운 글쓰기`;

  try {
    const response = await fetch(
      "https://generativelanguage.googleapis.com/v1beta/models/gemini-2.5-flash:generateContent?key=" +
        API_KEY,
      {
        method: "POST",
        headers: {
          "Content-Type": "application/json",
        },
        body: JSON.stringify({
          contents: [
            {
              parts: [
                {
                  text: fullPrompt,
                },
                ...imageParts,
              ],
            },
          ],
          generationConfig: {
            temperature: 0.7,
            topK: 40,
            topP: 0.95,
            maxOutputTokens: 12000,
          },
        }),
      }
    );

    if (!response.ok) {
      const errorData = await response.json();
      throw new Error(`API 오류: ${JSON.stringify(errorData)}`);
    }

    const data = await response.json();
    const generatedText =
      data.candidates?.[0]?.content?.parts?.[0]?.text || "";

    if (!generatedText) {
      throw new Error("API에서 텍스트를 생성하지 못했습니다.");
    }

    return generatedText;
  } catch (error) {
    console.error("Gemini API 오류:", error);
    throw error;
  }
};
